    return getDeltaT(this)
}

// TT-UT1
fun getDeltaT(mjd: MJD): Double {
    val deltaTInSecs = when (mjd.value) {
        in (41714.0 - 365.0)..(59274.0 + 365.0) -> {
            currentDeltaTInterpolator.getLinearValue(mjd.value)
        }
        in (58484.0 - 365.0)..(61680.0 + 365.0) -> {
            predictionDeltaTInterpolator.getLinearValue(mjd.value)
        }
        else -> null
    }
    if (deltaTInSecs != null) return deltaTInSecs
    return getDeltaT(mjd.toInstant())
}

// TT-UT1
private fun getDeltaT(instant: Instant): Double {
    val dateTime = instant.toLocalDateTime(TimeZone.UTC)

    val y = dateTime.year + (dateTime.monthNumber - 0.5) / 12.0

    val deltaT = when (dateTime.year) {
        in 2005..2050 -> {
            val t = y - 2000.0
            62.92 + (0.32217 + 0.005589 * t) * t
        }
        in -1999..-500 -> -20.0 + 32.0 * ((dateTime.year - 1820.0) / 100.0 * (dateTime.year - 1820.0) / 100.0)
        in -500..500 -> {
            val u = y / 100.0
            10583.6 + (-1014.41 + (33.78311 + (-5.952053 + (-0.1798452 + (0.022174192 + 0.0090316521 * u) * u) * u) * u) * u) * u
        }
        in 500..1600 -> {
            val u = (y - 1000.0) / 100.0
            1574.2 + (-556.01 + (71.23472 + (0.319781 + (-0.8503463 + (-0.005050998 + 0.0083572073 * u) * u) * u) * u) * u) * u
        }
        in 1600..1700 -> {
            val u = y - 1600.0
            120.0 + (-0.9808 + (-0.01532 + u / 7129.0) * u) * u
        }
        in 1700..1800 -> {
            val u = y - 1700.0
            8.83 + (0.1603 + (-0.0059285 + (0.00013336 - u / 1174000.0) * u) * u) * u
        }
        in 1800..1860 -> {
            val u = y - 1800.0
            13.72 + (-0.332447 + (0.0068612 + (0.0041116 + (-0.00037436 + (0.0000121272 + (-0.0000001699 + 0.000000000875 * u) * u) * u) * u) * u) * u) * u
        }
        in 1860..1900 -> {
            val u = y - 1860.0
            7.62 + (0.5737 + (-0.251754 + (0.01680668 + (-0.0004473624 + u / 233174.0) * u) * u) * u) * u
        }
        in 1900..1920 -> {
            val u = y - 1900.0
            -2.79 + (1.494119 + (-0.0598939 + (0.0061966 - 0.000197 * u) * u) * u) * u
        }
        in 1920..1941 -> {
            val u = y - 1920.0
            21.20 + (0.84493 + (-0.076100 + 0.0020936 * u) * u) * u
        }
        in 1941..1961 -> {
            val u = y - 1950.0
            29.07 + (0.407 + (-1 / 233.0 + u / 2547.0) * u) * u
        }
        in 1961..1986 -> {
            val u = y - 1975.0
            45.45 + (1.067 + (-1 / 260.0 + -u / 718.0) * u) * u
        }
        in 1086..2005 -> {
            val u = y - 2000.0
            63.86 + (0.3345 + (-0.060374 + (0.0017275 + (0.000651814 + 0.00002373599 * u) * u) * u) * u) * u
        }
        in 2050..2150 -> -20.0 + 32.0 * ((y - 1820.0) / 100.0).pow(2.0) - 0.5628 * (2150.0 - y)
        in 2150..3000 -> -20.0 + 32.0 * ((y - 1820.0) / 100.0).pow(2.0)
        else -> -20.0 + 32.0 * ((y - 1820.0) / 100.0).pow(2.0)
    }

    return deltaT
}

